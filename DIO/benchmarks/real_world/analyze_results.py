#!/usr/bin/env python3
"""
Parse Locust CSV outputs from benchmarks/results_final into results_summary.json.

Usage:
  python benchmarks/real_world/analyze_results.py
  python benchmarks/real_world/analyze_results.py --results-dir benchmarks/results_final
"""

import argparse
import csv
import json
import os
import re
from datetime import datetime, timezone

SLO_MS = 2000.0

STRATEGY_MAP = {
    "nlms": "NLMS",
    "round_robin": "RoundRobin",
    "least_load": "LeastLoaded",
    "rls": "RLS",
}

DATASET_MAP = {
    "sharegpt": "ShareGPT",
    "arxiv": "Arxiv",
    "azure_code": "Azure",
}


def parse_stats_csv(path):
    with open(path, newline="", encoding="utf-8") as f:
        rows = list(csv.DictReader(f))
    for row in rows:
        if row.get("Type") == "POST" and row.get("Name") == "/api/generate":
            return {
                "request_count": int(float(row["Request Count"])),
                "failure_count": int(float(row["Failure Count"])),
                "p50_ms": float(row["50%"]),
                "p95_ms": float(row["95%"]),
                "p99_ms": float(row["99%"]),
                "median_ms": float(row["Median Response Time"]),
                "avg_ms": float(row["Average Response Time"]),
                "rps": float(row["Requests/s"]),
            }
    return None


def parse_failures_csv(path):
    if not os.path.exists(path):
        return 0
    with open(path, newline="", encoding="utf-8") as f:
        rows = list(csv.DictReader(f))
    return len(rows)


def infer_test_id(filename):
    # e.g. nlms_sharegpt_4w_stats.csv
    base = os.path.basename(filename).replace("_stats.csv", "")
    m = re.match(r"^(nlms|round_robin|least_load|rls)_(.+)_(\d+)w$", base)
    if not m:
        return None
    strategy_key, dataset_key, workers = m.group(1), m.group(2), int(m.group(3))
    return {
        "strategy_key": strategy_key,
        "strategy": STRATEGY_MAP.get(strategy_key, strategy_key),
        "dataset_key": dataset_key,
        "dataset": DATASET_MAP.get(dataset_key, dataset_key),
        "workers": workers,
        "id": base,
    }


def slo_attainment(p50_ms, p99_ms):
    # Approximate: if p99 under SLO, high attainment; scale by median as proxy when no per-req TTFT log
    if p99_ms <= SLO_MS:
        return min(100.0, 100.0 * (1.0 - (p50_ms / SLO_MS) * 0.1))
    return max(0.0, 100.0 * (SLO_MS / p99_ms))


def analyze(results_dir, out_json, out_tex):
    tests = []
    for name in sorted(os.listdir(results_dir)):
        if not name.endswith("_stats.csv"):
            continue
        meta = infer_test_id(name)
        if meta is None:
            continue
        stats_path = os.path.join(results_dir, name)
        stats = parse_stats_csv(stats_path)
        if stats is None:
            continue
        fail_path = stats_path.replace("_stats.csv", "_failures.csv")
        extra_failures = parse_failures_csv(fail_path)
        total_fail = stats["failure_count"] + extra_failures
        fail_rate = (total_fail / stats["request_count"] * 100.0) if stats["request_count"] else 0.0
        slo = slo_attainment(stats["p50_ms"], stats["p99_ms"])
        tests.append({
            **meta,
            **stats,
            "fail_rate_pct": round(fail_rate, 2),
            "slo_attainment_pct": round(slo, 1),
            "p99_s": round(stats["p99_ms"] / 1000.0, 2),
        })

    summary = {
        "generated_at": datetime.now(timezone.utc).isoformat(),
        "results_dir": results_dir,
        "slo_ttft_ms": SLO_MS,
        "methodology": {
            "model": "meta-llama/Llama-3.2-3B-Instruct",
            "workers": 4,
            "duration_s": 90,
            "users": 20,
            "warmup_s": 30,
        },
        "tests": tests,
    }

    os.makedirs(os.path.dirname(out_json) or ".", exist_ok=True)
    with open(out_json, "w", encoding="utf-8") as f:
        json.dump(summary, f, indent=2)

    if out_tex:
        lines = [
            "% Auto-generated by analyze_results.py — do not edit by hand",
            "\\begin{table}[t]",
            "\\centering",
            "\\scriptsize",
            "\\begin{tabular}{llrrrrr}",
            "\\toprule",
            "Strategy & Dataset & RPS & p50 (ms) & p99 (ms) & SLO \\% & Fail \\% \\\\",
            "\\midrule",
        ]
        for t in tests:
            if t["workers"] != 4:
                continue
            lines.append(
                f"{t['strategy']} & {t['dataset']} & {t['rps']:.2f} & {t['p50_ms']:.0f} & {t['p99_ms']:.0f} & "
                f"{t['slo_attainment_pct']:.1f} & {t['fail_rate_pct']:.1f} \\\\"
            )
        lines += ["\\bottomrule", "\\end{tabular}", "\\caption{Locust benchmark summary (4 workers).}", "\\end{table}"]
        with open(out_tex, "w", encoding="utf-8") as f:
            f.write("\n".join(lines) + "\n")

    print(f"Wrote {len(tests)} test results to {out_json}")
    return summary


def main():
    root = os.path.dirname(os.path.dirname(os.path.dirname(os.path.abspath(__file__))))
    parser = argparse.ArgumentParser()
    parser.add_argument("--results-dir", default=os.path.join(root, "benchmarks", "results_final"))
    parser.add_argument("--out-json", default=os.path.join(root, "benchmarks", "results_summary.json"))
    parser.add_argument("--out-tex", default=os.path.join(root, "benchmarks", "results_table.tex"))
    args = parser.parse_args()
    analyze(args.results_dir, args.out_json, args.out_tex)


if __name__ == "__main__":
    main()