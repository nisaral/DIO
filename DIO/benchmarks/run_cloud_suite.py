def cleanup():
    print("\n🧹 Cleaning up processes...")
    for p in processes:
        if p.poll() is None:
            p.terminate()
            try:
                p.wait(timeout=2)
            except subprocess.TimeoutExpired:
                p.kill()
    subprocess.run("pkill -f 'dio-manager'", shell=True)
    subprocess.run("pkill -f 'worker_gpu.py'", shell=True)

