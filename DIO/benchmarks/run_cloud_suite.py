def start_workers(worker_list):
    print(f"   🔹 Spawning {len(worker_list)} Workers...")
    for w in worker_list:
        cmd = [
            sys.executable, WORKER_SCRIPT,
            "--worker-id", w["id"],
            "--tier", w.get("tier", "large"),
            "--vram", str(w.get("vram", 16384)),
            "--latency-mult", str(w.get("slow_factor", 1.0))
        ]
        if w.get("mock"): cmd.append("--mock")
        
        # Pass MODEL_ID to worker via environment variable
        worker_env = os.environ.copy()
        worker_env["MODEL_ID"] = MODEL_ID
        p = subprocess.Popen(cmd, stdout=subprocess.DEVNULL, stderr=subprocess.DEVNULL, env=worker_env)
        processes.append(p)
    time.sleep(5) 

def run_locust_test(config, csv_base):
    print(f"   🚀 Running Locust ({config['duration']})...")
    env = os.environ.copy()
    env["TEST_MODE"] = config['mode']
    env["LOG_FILE"] = f"{csv_base}_stats.csv"
    env["MODEL_ID"] = MODEL_ID # Pass model to Locust
    if "env" in config: env.update(config["env"])
    
    locust_script = config.get("locustfile", LOCUST_FILE)
    cmd = [
        "locust", "-f", locust_script,
        "--headless", "--users", str(config['users']),
        "--spawn-rate", str(config['rate']),
        "--run-time", str(config['duration']),
        "--host", MANAGER_URL,
        "--csv", csv_base
    ]
    subprocess.run(cmd, env=env, check=True, stdout=subprocess.DEVNULL, stderr=subprocess.DEVNULL)

def run_probe_test(config):
    print(f"   🚀 Running Probe Loop ({config['duration']}s)...")
    start_time = time.time()
    while time.time() - start_time < config['duration']:
        try:
            requests.post(f"{MANAGER_URL}/api/generate", json={
                "prompt": "test", "model_id": "test", "tier": "large"
            })
            time.sleep(0.1)
        except: time.sleep(0.1)

def main():
    prepare_environment()
    
    if os.path.exists(RESULTS_DIR): shutil.rmtree(RESULTS_DIR)
    os.makedirs(RESULTS_DIR)
    
    summary = []
    
    try:
        for test in TEST_SUITE:
            print(f"\n▶️  Test: {test['id']} ({test['desc']})")
            cleanup()
            start_manager()
            start_workers(test['workers'])
            
            csv_base = os.path.join(RESULTS_DIR, test['id'])
            
            if test['type'] == 'LOCUST':
                run_locust_test(test, csv_base)
                # Simple metric extraction for summary
                try:
                    df = pd.read_csv(f"{csv_base}_stats.csv")
                    p99 = df["latency_ms"].quantile(0.99)
                    summary.append({"Test": test['id'], "P99 (ms)": f"{p99:.2f}"})
                except:
                    summary.append({"Test": test['id'], "P99 (ms)": "N/A"})
            else:
                run_probe_test(test)
                summary.append({"Test": test['id'], "P99 (ms)": "Probe"})
                
    finally:
        cleanup()
        
    print("\n📊 Final Results:")
    print(pd.DataFrame(summary))
    print(f"Detailed logs saved to {RESULTS_DIR}/")

if __name__ == "__main__":
    main()


